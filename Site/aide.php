	Comment jouer ?<br/>
	Pour jouer, il faut vous trouver sur la page d'accueil du site. Une application contenant le jeu se lance et une demande de pseudonyme apparaît.<br/>
	Entrez un pseudonyme (il restera temporaire) puis vous serez redirigés sur l'explorateur de partie. <br/>
	Vous pouvez créer une nouvelle partie ou en rejoindre une. Lorsqu'une partie a atteint le nombre de joueur requis, l'hôte de la  partie peut la lancée.<br/>
	Après la fin de la partie, vous serez automatique redirigé sur l'explorateur de parties.
