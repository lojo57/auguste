package proelio_client;


/**
 * This is simply a couple of integers. Ex: it can indicates the position of a pixel.
 * Actually the class "Dimension" do the same.  
 */
public class XY {

	public int x;
	public int y;
	
	public XY(){
		x=0;
		y=0;
	}
	
	public XY(int i,int j){
		x=i;
		y=j;
	}
	
	
}
